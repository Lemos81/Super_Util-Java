package Exemplos;

/**
 *
 * @author Flávio
 */
public class Incremento {
    
    public static void main(String[] args){
        
        int a=1,b=1;
        
        System.out.println("Valor inicial de 'a' é: "+a);
        System.out.println("Valor inicial de 'b' é: "+b);
        System.out.println("Incrementando a++");
        ++a;
        System.out.println("Cecrementando b--");
        --b;
        
        System.out.printf("Agora a = "+a+"\n");
        System.out.printf("Agora b = "+b+"\n");
        
    }
}
